public class operator
{
	public static void main(String[] args)
	{
		int	a = 50;
		int	b = 50;

		System.out.println("a == b ? " + (a == b));
		System.out.println("a > b ? " + (a > b));
		System.out.println("a < b ? " + (a < b));
		System.out.println("a == b || a > b ? " + ((a == b) || (a > b)));
		System.out.println("a != 50 ? " + (a != 50));
	}
}
